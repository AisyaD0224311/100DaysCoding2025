package pkg100dayscoding2025;
public class day75 {
    public static void main(String[] args) {
        String teks = "Halo JW";
        int length = teks.length();
        String upper = teks.toUpperCase();
        String lower = teks.toLowerCase();
        System.out.println("Teks asli      : " + teks);
        System.out.println("Panjang teks   : " + length);
        System.out.println("Huruf kapital  : " + upper);
        System.out.println("Huruf kecil    : " + lower);
    }
}
