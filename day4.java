package pkg100dayscoding2025;
public class day4 {
    public static void main(String[] args) {
        // \n untuk spasi
        System.out.println("Halo\nNama saya Aisya");
        
        // \t untuk tab
        System.out.println("Umur :\t18");
        
        // \" & \'menampilkan tanda kutip
        System.out.println("\"Saya tinggal didesa paku\"");
        System.out.println("\'Tepatnya di dusun passubbe\'");
        
        // \\menampilkan backlash
        System.out.println("Folder : C:\\Users\\Aisya\\Documents");
        
        //\b menghapus satu karakter sebelumnya
        System.out.println("Saya darii\b kelas informatika B");
    }
}
